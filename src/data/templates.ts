export function getTemplate(id: string): Template {
  return templates.find((template) => template.id === id) ?? templates[0]
}

export function createStateFromTemplate(template: Template): AppState {
  return {
    version: 1,
    templateId: template.id,
    title: template.title,
    subtitle: template.subtitle,
    themeColor: template.themeColor,
    backgroundId: template.backgroundId ?? 'scrapbook',
    frameId: template.frameId ?? 'sticker',
    bottleShapeId: template.bottleShapeId ?? 'classic',
    showPercent: true,
    bottles: template.bottles.map((bottle) => ({ ...bottle })),
    updatedAt: Date.now(),
  }
}
