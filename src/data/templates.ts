function makeBottles(labels: string[]): Bottle[] {
  return labels.slice(0, 36).map((label, index) => ({
    id: `bottle-${index + 1}`,
    label,
    value: 0,
  }))
}

function makeBlankBottles(): Bottle[] {
  return Array.from({ length: 36 }, (_, index) => ({
    id: `bottle-${index + 1}`,
    label: `标签${index + 1}`,
    value: 0,
  }))
}

